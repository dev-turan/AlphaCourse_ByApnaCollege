// public class
public class Output_inJAVA {

	// main function
	public static void main(String[] args) {
		System.out.print("Hello World");  // for output
		System.out.print("1 2 3 4 5");  
		System.out.print("!@#$%^");
		
		// for next line 
		System.out.println("Hello World");
		System.out.println("1 2 3 4 5");
		System.out.println("!@#$%^");
		
		System.out.print("Hello World \n"); // '\n' for next line
		System.out.println("Hello World");
	}

}
