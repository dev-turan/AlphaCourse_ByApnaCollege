
public class Ques1 {
	public static void main(String[] args) {
        int count = 0;
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello");
            i += 2;
            count++;
        }
        System.out.println("The number of times 'Hello' is printed: " + count);
    }
}
