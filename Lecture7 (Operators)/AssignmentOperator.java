
public class AssignmentOperator {

	public static void main(String[] args) {
		//  =, +=, -=, *=, /=
		
		int a = 10;
		a += 1;
		
		int b = 15;
		b-= 1;
		
		int c = 20;
		c *= 2;
		
		int d = 30;
		d/=5;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

	}

}
