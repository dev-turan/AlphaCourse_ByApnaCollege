
public class RelationalOperators {

	public static void main(String[] args) {
	   // Java includes six relational operators:
       //    == (equal to)
       //	 != (not equal to)
       //	 < (less than)
       //	 > (greater than)
       //	 <= (less than or equal to)
       //	 >= (greater than or equal to)
		
		int a = 10;
		int b = 3;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);

	}

}
